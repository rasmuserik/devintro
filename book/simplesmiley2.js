function eye() {
    turtle.forward();
    turtle.left();
    turtle.forward();
    turtle.left();
    turtle.forward();
    turtle.left();
    turtle.forward();
};

function mouth() {
    turtle.jump();
    turtle.forward();
    turtle.left();
    turtle.forward();
    turtle.forward();
    turtle.forward();
    turtle.left();
    turtle.forward();
    turtle.jump();
};

eye();
mouth();
eye();
